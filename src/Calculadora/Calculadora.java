    public void setTable() {
        PreparedStatement consul = null; 
        ResultSet resultados = null; 
        ResultSetMetaData infobd = null; 
        try {
            DefaultTableModel modelo = new DefaultTableModel(); 
            tabla.setModel(modelo);
            modelo.addColumn("operacion");
            modelo.addColumn("resultado"); 
            com.mysql.jdbc.Connection sql = new conexion().getConexion();
            consul = (PreparedStatement) sql.prepareStatement("SELECT * FROM operaciones");
            resultados = consul.executeQuery();
            infobd = consul.getMetaData();
            int column = infobd.getColumnCount(); 
            Object filas[] = new Object[column]; 
            while (resultados.next()) {
                for (int i = 0; i < column; i++) {
                    filas[i] = resultados.getObject(i + 1);
                }
                modelo.addRow(filas);
            }
        } catch (SQLException e) {
            JOptionPane.showConfirmDialog(null, "Hubo un error:" + "Revisar conexión a la BD o a la tabla", "Error", 0, 0);

        }
    }

