    public void guardarDatos(String calculo, String Resultado) {
        try {
            java.sql.Connection sql = new conexion().getConexion();
            PreparedStatement pr = null;
            ResultSet rs = null;
            pr = (PreparedStatement) sql.prepareStatement("INSERT INTO operaciones VALUES(?, ?)");
            pr.setString(1, calculo);
            pr.setString(2, Resultado);
            pr.execute();
            setTable();
        } catch (SQLException e) {
            System.err.println(e.toString());
        }
    }
    private void igualActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_igualActionPerformed
        String respu;
        verif();
        num2 = Double.parseDouble(pantalla.getText());
        try {
                if (operacion == "+") {
                    result = num1 + num2;
                    respu = String.format("%.2f", result);
                    pantalla.setText(respu);
                    guardarDatos(String.valueOf(num1) + "+" + String.valueOf(num2), respu);
                } else if (operacion == "-") {
                    result = num1 - num2;
                    respu = String.format("%.2f", result);
                    pantalla.setText(respu);
                    guardarDatos(String.valueOf(num1) + "-" + String.valueOf(num2), respu);
                } else if (operacion == "*") {
                    result = num1 * num2;
                    respu = String.format("%.2f", result);
                    pantalla.setText(respu);
                    guardarDatos(String.valueOf(num1) + "*" + String.valueOf(num2), respu);
                } else if (operacion == "/") {
                    result = num1 / num2;
                    respu = String.format("%.2f", result);
                    pantalla.setText(respu);
                    guardarDatos(String.valueOf(num1) + "/" + String.valueOf(num2), respu);
                }
        }
        catch(Exception e){
            JOptionPane.showConfirmDialog(null, "Hubo un error en la operación" + e);
        }


    }//GEN-LAST:event_igualActionPerformed

